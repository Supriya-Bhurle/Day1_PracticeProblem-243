package com.bridgelabz.javafundamental;

public class DefaultValue {

    byte b;
    short s;
    int i;
    long l;
    double d;
    float f;

    char ch;
    boolean bu;


    public static void main(String[] args) {
        DefaultValue def = new DefaultValue();


        System.out.println(def.b);
        System.out.println(def.s);
        System.out.println(def.i);
        System.out.println(def.l);
        System.out.println(def.d);
        System.out.println(def.f);
        System.out.println(def.ch);
        System.out.println(def.bu);
    }
}